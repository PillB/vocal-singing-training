/**
 * Generate strong passwords (one at a time via crypto) for internal accounts.
 * Writes:
 *   - docs/INTERNAL-TEST-ACCOUNTS.md  (plaintext — gitignored, never commit)
 *   - js/auth-users.generated.js      (usernames + salted SHA-256 hashes only)
 *
 * Usage: node qa/generate-test-accounts.mjs
 */
import crypto from "crypto";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.join(__dirname, "..");

const ALPHABET =
  "ABCDEFGHJKLMNPQRSTUVWXYZabcdefghijkmnopqrstuvwxyz23456789!@#$%&*-_=+";

/** Cryptographically strong password — one generation per call */
function generateStrongPassword(length = 20) {
  const bytes = crypto.randomBytes(length);
  let out = "";
  for (let i = 0; i < length; i++) {
    out += ALPHABET[bytes[i] % ALPHABET.length];
  }
  // Ensure mixed classes for policy compliance
  if (!/[A-Z]/.test(out)) out = "A" + out.slice(1);
  if (!/[a-z]/.test(out)) out = out.slice(0, 2) + "a" + out.slice(3);
  if (!/[0-9]/.test(out)) out = out.slice(0, 4) + "7" + out.slice(5);
  if (!/[!@#$%&*\-_=+]/.test(out)) out = out.slice(0, 6) + "!" + out.slice(7);
  return out;
}

function saltHash(password, salt) {
  return crypto.createHash("sha256").update(`${salt}:${password}`, "utf8").digest("hex");
}

function makeUser(username, role, displayName) {
  // Generate password in isolation (one random draw per account)
  const password = generateStrongPassword(20);
  const salt = crypto.randomBytes(16).toString("hex");
  const hash = saltHash(password, salt);
  return {
    username,
    role,
    displayName,
    password, // only for secrets file
    salt,
    hash
  };
}

const accounts = [];
// 2 admins
accounts.push(makeUser("admin.pablo", "admin", "Admin Pablo"));
accounts.push(makeUser("admin.ops", "admin", "Admin Ops"));
// 10 friends & family testers
for (let i = 1; i <= 10; i++) {
  const n = String(i).padStart(2, "0");
  accounts.push(makeUser(`tester.ff${n}`, "tester", `Friends & Family ${n}`));
}

